package package1;

public class IfEleseExample {

	public static void main(String[] args) {
		
		int wal=20;
		
		int st1=20;
		int st2=20;
		int st3=18;
		
				
		if(wal==st1){
			
			System.out.println("I buy the book from store 1");
		}
		
		else if(wal==st2){
			
			System.out.println("I buy the book from store 2");
		}
		else if(wal>=st3){
			
			System.out.println("I buy the book from store 3");
		}
		else
			System.out.println("I do not buy book");
		

	}

}
