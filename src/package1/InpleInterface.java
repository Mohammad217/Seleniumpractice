package package1;

public class InpleInterface implements  InterfaceConcept{

	public static void main(String[] args) {
		InpleInterface ic=new InpleInterface();
		ic.idea();
		ic.idea1();
		ic.idea2();
		

	}

	
	public void idea1() {
		
		System.out.println("This is first business idea");
		
	}

	
	public void idea2() {
		
		System.out.println("This is second business idea");
		
	}

	
	public void idea() {
		
		System.out.println("This is thrid business idea");
		
	}

}
